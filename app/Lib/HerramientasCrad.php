    function ImageCarousel($array)
    {
        $modal2 = "";
        foreach ($array as $key => $value) {

            if (str::contains($value->filename, ['.png', '.jpg', 'gif'])) {
                $codebase64 = base64_encode( Storage::get('avatars/tmp/' . $value->folder . '/' . $value->filename));
                $modal2 = $modal2 . '
                <div class="hidden duration-200 ease-linear" data-carousel-item="active">
                    <img src="data:image/jpeg;base64,' . $codebase64 . '" class="absolute block w-full -translate-x-1/2 -translate-y-1/2 top-1/2 left-1/2" alt="...">
                </div>';
            }
        }

        return '
                    <div id="animation-carousel" class="relative" data-carousel="static">
                    <!-- Carousel wrapper -->
                    <div class="relative h-56 overflow-hidden rounded-lg md:h-96">
                    ' . $modal2 . '
                    </div>
                    <!-- Slider controls -->
                    <button type="button" class="absolute top-0 left-0 z-30 flex items-center justify-center h-full px-4 cursor-pointer group focus:outline-none" data-carousel-prev>
                        <span class="inline-flex items-center justify-center w-8 h-8 rounded-full sm:w-10 sm:h-10 bg-white/30 dark:bg-gray-800/30 group-hover:bg-white/50 dark:group-hover:bg-gray-800/60 group-focus:ring-4 group-focus:ring-white dark:group-focus:ring-gray-800/70 group-focus:outline-none">
                            <svg aria-hidden="true" class="w-5 h-5 text-white sm:w-6 sm:h-6 dark:text-gray-800" fill="none" stroke="currentColor" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 19l-7-7 7-7"></path></svg>
                            <span class="sr-only">Previous</span>
